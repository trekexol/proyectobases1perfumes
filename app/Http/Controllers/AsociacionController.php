     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        
        $a = new asociacion_nacional();
        $a->nombre = $request->get('nombre');
        $a->region = $request->get('region');
        $a->iniciales = $request->get('iniciales');
        $a->id_pais = $request->get('pais');
        $a->save();
        return \redirect('inserts/asociaciones/create');
    }

